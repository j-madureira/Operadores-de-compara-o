var b = 1;
console.log(a == b);
console.log(a === b);


//Comparando se os valores são diferentes/ Comparando se os valores e tipos são diferentes
var c = 2;
var d = 2;
console.log( c != d);
console.log( c !== d);


//Comparando se o valor é maior que o outro / Comparando se o valor é maior ou igual
